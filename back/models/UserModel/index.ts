import { model, Schema } from "mongoose";
import { IUser } from "./types";

const UserSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  nickname: {
    type: String,
    required: true,
  },
});

export const UserModel = model<IUser>("User", UserSchema);
